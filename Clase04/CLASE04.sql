SELECT *
FROM HR.EMPLOYEES
WHERE UPPER(LAST_NAME) = 'SMITH' AND SALARY > 7500;


SELECT *
FROM HR.EMPLOYEES
WHERE DEPARTMENT_ID = 10 OR DEPARTMENT_ID = 20;

SELECT *
FROM HR.EMPLOYEES
WHERE DEPARTMENT_ID IN (10,20);


select *
from HR.EMPLOYEES
where SUBSTR(FIRST_NAME,1,1) BETWEEN 'A' and 'L'
order by 2;



-- OPERACIONES CON VALORES NULOS

