SELECT D.DEPARTMENT_ID, D.DEPARTMENT_NAME, COUNT(1) EMPS
FROM HR.DEPARTMENTS D
JOIN HR.EMPLOYEES E ON D.DEPARTMENT_ID = E.DEPARTMENT_ID
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME
ORDER BY 3 DESC;

SELECT D.DEPARTMENT_ID, D.DEPARTMENT_NAME, COUNT(1) EMPS
FROM HR.DEPARTMENTS D
JOIN HR.EMPLOYEES E ON D.DEPARTMENT_ID = E.DEPARTMENT_ID
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME
HAVING COUNT(1) > 10
ORDER BY 3 DESC;









